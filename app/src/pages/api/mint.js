export async function mintToken(walletPrivateKey, to, tokenId, uri) {
    const provider = new ethers.providers.JsonRpcProvider("YOUR_RPC_URL_HERE"); // e.g., Infura or Alchemy URL
    const wallet = new ethers.Wallet(walletPrivateKey, provider);
    const contract = new ethers.Contract(CONTRACT_ADDRESS, ABI, wallet);

    try {
        const tx = await contract.safeMint(to, tokenId, uri);
        await tx.wait();
        return { success: true, txHash: tx.hash };
    } catch (error) {
        console.error("Error minting token:", error);
        return { success: false, error: error.message };
    }
}
